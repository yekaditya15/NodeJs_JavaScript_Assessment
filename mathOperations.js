// Using const for a constant value
const baseValue = 10;

// Using let for block-scoped variable
let multiplier = 2;

// Using var for function-scoped variable (not recommended in modern JS)
var additionalValue = 5;

// Performing calculations
let result = baseValue * multiplier + additionalValue;

// Printing the results to the console
console.log("Base Value (const):", baseValue);
console.log("Multiplier (let):", multiplier);
console.log("Additional Value (var):", additionalValue);
console.log("Final Result:", result);
